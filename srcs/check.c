#include "../includes/ft_printf.h"

int		ft_is_type(char c)
{
    if (c == 's' || c == 'd' || c == 'o' || c == 'c')
        return (1);
    return (0);
}

int		ft_is_flags(char c)
{
    if (c == ' ' || c == '+' || c == '-' || c == '#' || c == '0')
        return (1);
    return (0);
}
